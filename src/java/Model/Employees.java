package Model;

import java.util.*;
import java.lang.*;

public class Employees {
    private int id;
    private String name;
    private boolean gender;
    private Date dob;
    private String createdBy;

    public Employees() {
    }

    public Employees(int id, String name, boolean gender, Date dob, String createdBy) {
        this.id = id;
        this.name = name;
        this.gender = gender;
        this.dob = dob;
        this.createdBy = createdBy;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isGender() {
        return gender;
    }

    public void setGender(boolean gender) {
        this.gender = gender;
    }

    public Date getDob() {
        return dob;
    }

    public void setDob(Date dob) {
        this.dob = dob;
    }

    public String getCreatedBy() {
        return createdBy;
    }

    public void setCreatedBy(String createdBy) {
        this.createdBy = createdBy;
    }
    
}
